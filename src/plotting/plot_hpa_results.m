function [performanceFigure, chartInfo] = plot_hpa_results( ...
        results, chartDir, figureVisible)
%PLOT_HPA_RESULTS Grafico IEEE: coppie MMSE/CNN per ciascun IBO.

    if nargin < 2 || isempty(chartDir)
        chartDir = fullfile(pwd, 'CHARTS');
    end
    if nargin < 3 || isempty(figureVisible)
        figureVisible = 'on';
    end
    if ~exist(chartDir, 'dir')
        mkdir(chartDir);
    end

    colors = ieee_colors(numel(results.IBODb));
    performanceFigure = figure('Color', 'w', 'Visible', figureVisible, ...
        'Units', 'inches', 'Position', [1 1 7.2 4.8], ...
        'PaperPositionMode', 'auto');
    ax = axes(performanceFigure);
    hold(ax, 'on');
    set(ax, 'YScale', 'log', 'FontName', 'Times New Roman', ...
        'FontSize', 9, 'LineWidth', 0.8, 'TickDir', 'in', ...
        'XMinorTick', 'on', 'YMinorTick', 'on', 'Box', 'on');

    mmseIdx = find(results.MethodNames == "MMSE", 1);
    cnnIdx = find(results.MethodNames == "CNN", 1);
    markerSet = {'o', 's', '^', 'd', 'v'};
    for iboIdx = 1:numel(results.IBODb)
        marker = markerSet{1 + mod(iboIdx - 1, numel(markerSet))};
        if ~isempty(mmseIdx)
            semilogy(ax, results.SNRDb, ...
                squeeze(results.PlotBER(iboIdx, :, mmseIdx)), '--', ...
                'Color', colors(iboIdx, :), 'LineWidth', 1.0, ...
                'Marker', marker, 'MarkerSize', 4, ...
                'MarkerFaceColor', 'w', ...
                'DisplayName', sprintf('IBO %+.0f dB - MMSE', ...
                results.IBODb(iboIdx)));
        end
        if ~isempty(cnnIdx)
            semilogy(ax, results.SNRDb, ...
                squeeze(results.PlotBER(iboIdx, :, cnnIdx)), '-', ...
                'Color', colors(iboIdx, :), 'LineWidth', 1.0, ...
                'Marker', marker, 'MarkerSize', 4, ...
                'MarkerFaceColor', colors(iboIdx, :), ...
                'DisplayName', sprintf('IBO %+.0f dB - CNN', ...
                results.IBODb(iboIdx)));
        end
    end

    grid(ax, 'on');
    ax.XMinorGrid = 'on';
    ax.YMinorGrid = 'on';
    ax.GridAlpha = 0.18;
    ax.MinorGridAlpha = 0.08;
    xlabel(ax, 'SNR (dB)', 'FontName', 'Times New Roman');
    ylabel(ax, 'Bit error rate (BER)', 'FontName', 'Times New Roman');
    xlim(ax, [0 35]);
    xticks(ax, 0:5:35);
    try
        ax.XAxis.MinorTickValues = 0:2:34;
    catch
    end
    ylim(ax, [6e-5 1]);
    title(ax, 'BER performance under Rapp HPA nonlinearity', ...
        'FontName', 'Times New Roman', 'FontSize', 10, 'FontWeight', 'normal');
    subtitle(ax, sprintf('IEEE 802.11a QPSK, Rapp p = %.1f', ...
        results.Smoothness), 'FontName', 'Times New Roman', 'FontSize', 9);
    legend(ax, 'Location', 'northeast', 'NumColumns', 2, ...
        'FontName', 'Times New Roman', 'FontSize', 8, 'Box', 'on');
    ax.Position = [0.11 0.20 0.86 0.69];
    annotation(performanceFigure, 'textbox', [0.11 0.035 0.86 0.07], ...
        'String', sprintf(['Fig. 1. MMSE (dashed) and CNN (solid) BER; ' ...
        'timestamp %s.'], results.Timestamp), 'EdgeColor', 'none', ...
        'HorizontalAlignment', 'center', 'FontName', 'Times New Roman', ...
        'FontSize', 8, 'Interpreter', 'none');

    baseName = sprintf('ber_%s', char(results.Timestamp));
    pngPath = fullfile(chartDir, [baseName '.png']);
    pdfPath = fullfile(chartDir, [baseName '.pdf']);
    figPath = fullfile(chartDir, [baseName '.fig']);
    exportgraphics(performanceFigure, pngPath, 'Resolution', 600, ...
        'BackgroundColor', 'white');
    exportgraphics(performanceFigure, pdfPath, 'ContentType', 'vector', ...
        'BackgroundColor', 'white');
    savefig(performanceFigure, figPath);
    chartInfo = struct('Files', string({pngPath; pdfPath; figPath}), ...
        'XLimDb', [0 35], 'YLimBER', [6e-5 1], ...
        'MinorTickStepDb', 2, 'LineWidthPt', 1.0, ...
        'MMSELineStyle', '--', 'CNNLineStyle', '-', ...
        'LegendLocation', 'northeast', 'XMinorGrid', true, ...
        'Background', 'white', 'Timestamp', results.Timestamp);
end

function colors = ieee_colors(count)
    palette = [0.0000 0.4470 0.7410; 0.8500 0.3250 0.0980; ...
        0.4660 0.6740 0.1880; 0.4940 0.1840 0.5560; ...
        0.3010 0.7450 0.9330; 0.6350 0.0780 0.1840];
    colors = palette(1 + mod(0:count-1, size(palette, 1)), :);
end
